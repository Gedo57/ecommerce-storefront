export function translateText(value, language = 'ar') {
  if (typeof value !== 'string') return value;
  return TRANSLATIONS[language]?.[value] ?? value;
}

function replaceTextNode(node, language) {
  const raw = node.nodeValue;
  if (!raw || !raw.trim()) return;
  const trimmed = raw.trim();
  const translated = translateText(trimmed, language);
  if (translated !== trimmed) {
    const leading = raw.match(/^\s*/)?.[0] ?? '';
    const trailing = raw.match(/\s*$/)?.[0] ?? '';
    node.nodeValue = `${leading}${translated}${trailing}`;
  }
}

function translateAttributes(element, language) {
  ['placeholder', 'title', 'aria-label'].forEach((attr) => {
    const value = element.getAttribute?.(attr);
    if (!value) return;
    const translated = translateText(value, language);
    if (translated !== value) {
      element.setAttribute(attr, translated);
    }
  });
}

export function applyTranslations(root, language = 'ar') {
  if (!root) return;

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.parentElement) return NodeFilter.FILTER_REJECT;
      const tag = node.parentElement.tagName;
      if (tag === 'SCRIPT' || tag === 'STYLE') return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });

  const textNodes = [];
  let current = walker.nextNode();
  while (current) {
    textNodes.push(current);
    current = walker.nextNode();
  }
  textNodes.forEach((node) => replaceTextNode(node, language));

  if (root.querySelectorAll) {
    root.querySelectorAll('*').forEach((element) => translateAttributes(element, language));
  }
}
